        Reclamation selected = tableReclamations.getSelectionModel().getSelectedItem();
        if (selected == null) {
            afficherErreur("S脙漏lectionne une r脙漏clamation !");
            return;
        }

        ChoiceDialog<String> dialog = new ChoiceDialog<>(
                selected.getEtat(), "En attente", "En cours", "R脙漏solu", "Rejet脙漏"
        );
        dialog.setTitle("Modifier le statut");
        dialog.setHeaderText("R脙漏clamation : " + selected.getObjet());
